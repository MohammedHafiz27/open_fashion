class AddressModel {
  String firstName;
  String lastName;
  String address;
  String city;
  String state;
  String zipCode;
  String phoneNumber;

  AddressModel({
    required this.firstName,
    required this.lastName,
    required this.address,
    required this.city,
    required this.state,
    required this.zipCode,
    required this.phoneNumber,
  });
}
